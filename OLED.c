void Delay(unsigned int time)
{
	unsigned int i, j;
	for (i = 0; i < time; i++)
		for (j = 0; j < 20000; j++)
			;
}
void Ins_trans(unsigned char command)
  {
    unsigned char counter;
	  _CS=0;
	  RS=0;
    for(counter=0;counter<8;counter++)
       {
	     SCL=0;
         SI=(command&0x80)>>7;
         command=command<<1;
	       _nop_();
		     SCL=1;
	     //  _nop_();
		     _nop_();
		     SCL=0;
        }
	RS=1;
   _CS=1;
  }
void Data_trans(unsigned char command)
  {
    unsigned char counter;
	  _CS=0;
	  RS=1;
    for(counter=0;counter<8;counter++)
       {
         SCL=0;
	     SI=(command&0x80)>>7;
		 command=command<<1;
	       _nop_();
		     SCL=1;
	      // _nop_();
		     _nop_();
		     SCL=0;
       }
	RS=1;
   _CS=1;
   }
void Column_set(unsigned char column)
          {
		     Ins_trans(0x10|(column>>4));
		     Ins_trans(0x00|(column&0x0f));
		  }
void Page_set(unsigned char page)
          {
		    Ins_trans(0xb0+page);
		  }
void Screen_FillClear(unsigned char FC)
{
	unsigned char page, column;
	for (page = 0; page < 8; page++)             //page loop
	{
		Page_set(page);
		Column_set(2);
		for (column = 0; column < 128; column++)	//column loop
			Data_trans(FC);
	}
}

void Picture_display(un8* ptr_pic, un8 colStart, un8 pageStart, un8 line, un8 col)
{
	un8 page, column;

	for (page = pageStart; page < pageStart + (line / 8) - 1; page++)        //page loop
	{
		Column_set(colStart);
		Page_set(page);

		for (column = 0; column < col; column++)	//column loop
			Data_trans(*ptr_pic++);
	}
}

